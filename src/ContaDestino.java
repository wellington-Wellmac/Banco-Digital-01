
public class ContaDestino {
	
	private Integer agenciaDestino;
	private Integer contaBancaria;
	private String bancoDestino;
	private String nomeFavorecido;
	
	public String getNomeFavorecido() {
		return nomeFavorecido;
	}
	public void setNomeFavorecido(String nomeFavorecido) {
		this.nomeFavorecido = nomeFavorecido;
	}
	public String getBancoDestino() {
		return bancoDestino;
	}
	public void setBancoDestino(String bancoDestino) {
		this.bancoDestino = bancoDestino;
	}
	public Integer getContaBancaria() {
		return contaBancaria;
	}
	public void setContaBancaria(Integer contaBancaria) {
		this.contaBancaria = contaBancaria;
	}
	public Integer getAgenciaDestino() {
		return agenciaDestino;
	}
	public void setAgenciaDestino(Integer agenciaDestino) {
		this.agenciaDestino = agenciaDestino;
	}

}
